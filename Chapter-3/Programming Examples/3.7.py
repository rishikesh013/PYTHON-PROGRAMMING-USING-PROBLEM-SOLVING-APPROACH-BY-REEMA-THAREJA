# 3.7 Write an program that demonstrates relational operators

x = int(input("Enter the first value: "))
y = int(input("Enter the second value: "))

print(f"{x} > {y}   =  {x > y}")
print(f"{x} == {y}  =  {x == y}")
print(f"{x} != {y}  =  {x != y}")
print(f"{x} < {y}   =  {x < y}")
print(f"{x} >= {y}  =  {x >= y}")
print(f"{x} <= {y}  =  {x <= y}")

