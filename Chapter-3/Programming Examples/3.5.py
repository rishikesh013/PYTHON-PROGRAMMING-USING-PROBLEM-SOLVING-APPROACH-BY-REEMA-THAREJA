# 3.5 Write a program to perform addition, subtraction, multiplication, division, integer division, and modulo division
# on two integer number

a = int(input("Enter the value for the first number: "))
b = int(input("Enter the value for the Second number: "))

print(f"The addition of two numbers are {a + b}")
print(f"The subtraction of the two number's are {a - b}")
print(f"The multiplication of the two number's are {a * b}")
print(f"The division of the two number's are {a / b:.2f}")
print(f"The integer division of the two numbers are {a // b}")
print(f"The modulo division of the two number's are {a % b}")

