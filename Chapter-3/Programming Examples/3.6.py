# 3.6 Write a program to perform addition, subtraction, division, and multiplication on two floating point numbers

a = float(input("Enter the first floating point number: "))
b = float(input("Enter the second floating point number: "))

print(f"Addition of the two floating point number is {a + b}")
print(f"Subtraction of the two floating point number is {a - b}")
print(f"Multiplication of the two floating point number is {a * b:.2f}")
print(f"Division of the two floating point number is {a / b:.2f}")
