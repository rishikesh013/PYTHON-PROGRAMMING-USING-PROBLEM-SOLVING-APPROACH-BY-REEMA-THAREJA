# 3.3 Write a program to perform string concatenation

first_word = "Hello "
second_word = "World "
words_list = ["hello", "world"]
print(f"The addition of the two strings is {first_word + second_word}")
print(f"Using the join method {''.join((first_word, second_word))}")
print(f"Using the join method {' '.join(words_list)}")
